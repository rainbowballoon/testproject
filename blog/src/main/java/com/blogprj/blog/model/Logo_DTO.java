package com.blogprj.blog.model;

public class Logo_DTO {
	private int logo_no;
	private String logo_img;
	private String logo_bgimg;
	private int blog_no;
	
	public int getLogo_no() {
		return logo_no;
	}
	public void setLogo_no(int logo_no) {
		this.logo_no = logo_no;
	}
	public String getLogo_img() {
		return logo_img;
	}
	public void setLogo_img(String logo_img) {
		this.logo_img = logo_img;
	}
	public String getLogo_bgimg() {
		return logo_bgimg;
	}
	public void setLogo_bgimg(String logo_bgimg) {
		this.logo_bgimg = logo_bgimg;
	}
	public int getBlog_no() {
		return blog_no;
	}
	public void setBlog_no(int blog_no) {
		this.blog_no = blog_no;
	}
	
}
